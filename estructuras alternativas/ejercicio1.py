#Algoritmo que pida dos números e indique si el primero 
# es mayor que el segundo o no.

numero1 = int(input("Diga un primer número: "))
numero2 = int(input("Diga un segundo número: "))

if (numero1 > numero2):
    print("El número %d es mayor que el número %d "%(numero1,numero2))
else:
    print("El número %d es menor que el número %d "%(numero1,numero2))



